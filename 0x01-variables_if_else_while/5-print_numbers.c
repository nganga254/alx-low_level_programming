#include <stdio.h>

/**
* main - Entry point
*
* description :  print whether the number stored
* in the variable n is positive or negative
*
* return 0(Always)                                                                                          
*/

int main(void)
{
	int x;
	for (x = 0 ; x <= 10 ; x++)
	{
		putchar(x);
	}
	putchar('\n');

return (0);
}
